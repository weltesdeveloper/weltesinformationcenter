			<p class="dropcap">Dalam Kurikulum 2013 tersebut, mata pelajaran wajib diikuti oleh seluruh peserta didik pada satu satuan pendidikan pada setiap satuan atau pun jenjang pendidikan. Sementara untuk mata pelajaran pilihan yang diikuti oleh peserta didik, dipilih sesuai dengan pilihan dari mereka. Kedua kelompok mata pelajaran bersangkutan (wajib dan pilihan) terutamanya dikembangkan dalam struktur kurikulum pendidikan tingkat menengah yakni SMA dan SMK.</p>
			<p><strong>Kurikulum 2013</strong> dirancang dengan karakteristik sebagai berikut :</p>
			
