					<li>Mengembangkan keseimbangan antara pengembangan sikap spiritual & sosial, rasa ingin tahu, kreativitas, kerjasama dengan
						kemampuan intelektual dan psikomotor.</li>
					<li>sekolah merupakan bagian dari masyarakat yang memberikan pengalaman belajar terencana dimana peserta didik menerapkan apa
						yang dipelajari di sekolah ke masyarakat dan memanfaatkan masyarakat sebagai sumber belajar.</li>
					<li>mengembangkan sikap, pengetahuan, dan keterampilan serta menerapkannya dalam berbagai situasi di sekolah dan masyarakat.</li>
					<li>memberi waktu yang cukup leluasa untuk mengembangkan berbagai sikap, pengetahuan, dan keterampilan.</li>
					<li>kompetensi inti kelas menjadi unsur pengorganisasi (organizing elements) kompetensi dasar, dimana semua kompetensi dasar
